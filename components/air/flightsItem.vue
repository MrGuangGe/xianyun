<template>
  <div class="flight-item">
    <div @click="isShow = !isShow">
      <!-- 显示的机票信息 -->
      <el-row type="flex" align="middle" class="flight-info">
        <el-col :span="6">
          <span>{{data.airline_name}}</span>
          {{data.flight_no}}
        </el-col>
        <el-col :span="12">
          <el-row type="flex" justify="space-between" class="flight-info-center">
            <el-col :span="8" class="flight-airport">
              <strong>{{data.dep_time}}</strong>
              <span>{{data.org_airport_name}}{{data.org_airport_quay}}</span>
            </el-col>
            <el-col :span="8" class="flight-time">
              <span>{{distanceOfTime}}</span>
            </el-col>
            <el-col :span="8" class="flight-airport">
              <strong>{{data.arr_time}}</strong>
              <span>{{data.dst_airport_name}}{{data.dst_airport_quay}}</span>
            </el-col>
          </el-row>
        </el-col>
        <el-col :span="6" class="flight-info-right">
          ￥
          <span class="sell-price">{{data.base_price/2}}</span>起
        </el-col>
      </el-row>
    </div>
    <div class="flight-recommend" v-if="isShow">
      <!-- 隐藏的座位信息列表 -->
      <el-row type="flex" justify="space-between" align="middle">
        <el-col :span="4">低价推荐</el-col>
        <el-col :span="20">
          <el-row
            type="flex"
            justify="space-between"
            align="middle"
            class="flight-sell"
            v-for="(item,index) in data.seat_infos"
            :key="index"
          >
            <el-col :span="16" class="flight-sell-left">
              <span>{{item.group_name}}</span>
              | {{item.supplierName}}
            </el-col>
            <el-col :span="5" class="price">￥{{item.org_settle_price}}</el-col>
            <el-col :span="3" class="choose-button">
              <el-button type="warning" size="mini" @click="choseTicket(data.id,item.seat_xid)">选定</el-button>
              <p>剩余：{{item.discount}}</p>
            </el-col>
          </el-row>
        </el-col>
      </el-row>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      // 低价推荐的显示与隐藏
      isShow: false
    };
  },
  // 对象的方式声明组件可以接收的属性
  props: {
    // 数据
    data: {
      // 声明属性的类型
      type: Object,
      // 声明属性的默认值
      default: {}
    }
  },
  computed: {
    // 计算出发时间与到达世间的时间间隔
    distanceOfTime() {
      // console.log(this.data);
      // 20:30  ---  22:50
      // 通过冒号将这样的时间22:50切割成字符串
      const start = this.data.dep_time.split(":"); // 出发时间
      const end = this.data.arr_time.split(":"); // 到达世间

      // 计算间隔多少分钟
      // + +end是为了隐式转换 否则字符串就会相连了
      let arrTime = end[0] * 60 + +end[1];
      let depTime = start[0] * 60 + +start[1];

      // 第二天到达的话需要加上24小时 否则会出现负数
      if (arrTime < depTime) {
        arrTime += 24 * 60;
      }

      const distance = arrTime - depTime;
      const hour = Math.floor(distance / 60);
      const min = distance % 60;
      // computed属性会return回一个字符串
      return `${hour}时${min}分`;
    }
  },
  methods: {
    choseTicket(id, seat_xid) {
      this.$router.push({
        path: "/air/order",
        query: {
          id,
          seat_xid
        }
      });
    }
  }
};
</script>

